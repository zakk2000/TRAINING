CREATE TABLE `BOARD` (
	`SEQ` INT(10) UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '게시물 순번',
	`TITLE` VARCHAR(500) NOT NULL COMMENT '게시물 제목',
	`CONT` LONGTEXT NOT NULL COMMENT '게시물 내용',
	`CREATED_BY` VARCHAR(50) NOT NULL COMMENT '등록자 ID',
	`CREATE_DATE` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '등록일자',
	`UPDATED_BY` VARCHAR(50) NULL DEFAULT NULL COMMENT '수정자 ID',
	`UPDATE_DATE` TIMESTAMP NULL DEFAULT NULL COMMENT '수정일자',
	PRIMARY KEY (`SEQ`)
)
COLLATE='utf8_general_ci'
AUTO_INCREMENT=0